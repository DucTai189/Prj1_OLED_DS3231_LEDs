static int fd;
static uint8_t framebuffer[BUFSIZE];

void oled_command(uint8_t cmd) {
    uint8_t buf[2] = {0x00, cmd};
    write(fd, buf, 2);
}

void oled_data(uint8_t* data, int len) {
    uint8_t buf[len + 1];
    buf[0] = 0x40;
    memcpy(&buf[1], data, len);
    write(fd, buf, len + 1);
}

void oled_init() 
{
    
    oled_command(0xA8); // Set multiplex ratio
    oled_command(0x1F); // 1/32 duty
    oled_command(0xDA); // Set COM pins hardware configuration
    oled_command(0x02); // Alternative COM pin config, disable left/right remap
    oled_command(0xD3); // Set display offset
    oled_command(0x00); // No offset
    oled_command(0x40); // Set display start line
    oled_command(0xA1); // Set segment re-map
    oled_command(0xC8); // Set COM output scan direction
    oled_command(0x81); // Set contrast control
    oled_command(0x7F); // Contrast value
    oled_command(0xA4); // Disable entire display on
    oled_command(0xA6); // Set normal display
    oled_command(0xD5); // Set display clock divide ratio
    oled_command(0x80); // Set clock divide ratio
    oled_command(0x8D); // Set charge pump
    oled_command(0x14); // Enable charge pump
    oled_command(0xAF); // Display ON

    
}

void oled_clear() {
    memset(framebuffer, 0x00, BUFSIZE);
}

void oled_draw_pixel(int x, int y) 
{
    if (x < 0 || x >= WIDTH || y < 0 || y >= HEIGHT) return;
    framebuffer[x + (y / 8) * WIDTH] |= (1 << (y % 8));
}

// x is the x-coordinate where the string starts (bit position)
// y is the y-coordinate where the string starts (bit position)
// c is the character to wiht lenghth 5x7 pixels ( 5 bits per character, 7 lines )
void oled_draw_char(int x, int y, char c) 
{
    const uint8_t *bitmap = font5x7[c - 32];

    if (c < 32 || c > 127) return;
    for (int col = 0; col < 5; col++) 
    {
        uint8_t line = bitmap[col];
        for (int row = 0; row < 7; row++) 
        {
            if (line & (1 << row)) 
            {
                oled_draw_pixel(x + col, y + row);
            }
        }
    }
}
// x is the x-coordinate where the string starts (bit position)
// y is the y-coordinate where the string starts (bit position)
void oled_draw_string(int x, int y, const char* str) 
{
    while (*str) 
    {

        oled_draw_char(x, y, *str++);
        x += 6;
    }
}

void oled_display() {
    for (int page = 0; page < PAGES; page++) {
        oled_command(0xB0 + page);
        oled_command(0x00);
        oled_command(0x10);
        oled_data(&framebuffer[page * WIDTH], WIDTH);
    }
}

void Init_OLED() 
{
    fd = open(I2C_DEV, O_RDWR);
    if (fd < 0 || ioctl(fd, I2C_SLAVE, OLED_ADDR) < 0) 
    {
        perror("I2C open/ioctl");
        exit(1);
    }

    oled_init();
    oled_clear();
    oled_display();

    
}